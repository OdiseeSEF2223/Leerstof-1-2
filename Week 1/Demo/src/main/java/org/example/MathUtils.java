package org.example;

public class MathUtils {

    // functie die het kwadraat berekend
    public static double kwadraat(double input){
        return input * input;
    }
    public double kwadraat2(double input){
        return input * input;
    }

    // functie die de vierkantswortel berekend
    public static double vierkantswortel(double input){
        return Math.sqrt(input);
    }
}
